public class Score {
    public static void main(String[] args) {
        int score = 80;

        if(score >= 90) System.out.println("とてもよくできますか");
        else if(score >= 70) System.out.println("よくできました");
        else if(score >= 50) System.out.println("まずまずです");
        else System.out.println("がんばりましょう");
    }    
}
